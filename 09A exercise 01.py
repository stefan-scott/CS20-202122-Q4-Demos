

def letter_grade(numeric_grade):
    # takes a grade as a number (0-100)
    # returns the associated letter grade
    if numeric_grade >= 90:
        return "A"
    elif numeric_grade >= 80:
        return "B"
    elif numeric_grade >= 70:
        return "C"
    elif numeric_grade >= 60:
        return "D"
    else:
        return "F"

        
    

